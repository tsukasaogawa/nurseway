List<InlineSpan> _buildSpans(String content) {
final List<InlineSpan> spans = [];
final lines = content.split('\n');
const normalStyle = TextStyle(fontSize: 16, color: Colors.black);
const subtitleStyle = TextStyle(fontSize: 18, fontWeight: FontWeight.bold, color: Colors.black);



for (var line in lines) {
  if (line.startsWith(RegExp(r'^\d+\.\s')) || line == '診療科一覧') {
    spans.add(TextSpan(text: '$line\n', style: subtitleStyle));
  } else {
    spans.add(TextSpan(text: '$line\n', style: normalStyle));
  }
}

return spans;
}
